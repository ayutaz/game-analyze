def main(repo_root=None) -> None:
    """Apply PATCHES to each data/games/*.json file.

    Parameters
    ----------
    repo_root:
        Optional repository root. When provided, the games directory is
        resolved as ``<repo_root>/data/games``. Defaults to the module-level
        ``GAMES_DIR``. This makes the function testable against a temporary
        copy of the data tree without mutating the real ``data/`` files.
    """
    if repo_root is not None:
        games_dir = Path(repo_root).resolve() / "data" / "games"
    else:
        games_dir = GAMES_DIR

    n_patched = 0
    missing = []
    for path in sorted(games_dir.glob("*.json")):
        with path.open(encoding="utf-8") as f:
            game = json.load(f)
        patch = PATCHES.get(game["id"])
        if not patch:
            missing.append(game["id"])
            continue
        changed = False
        for k, v in patch.items():
            if game.get(k) != v:
                game[k] = v
                changed = True
        if changed:
            with path.open("w", encoding="utf-8") as f:
                json.dump(game, f, ensure_ascii=False, indent=2)
                f.write("\n")
            n_patched += 1

    print(f"patched {n_patched} game files (out of {len(PATCHES)} entries in PATCHES)")
    if missing:
        print(f"games WITHOUT a patch entry (id list): {missing}")
    print("now run: python3 scripts/build_aggregate.py")


if __name__ == "__main__":
    main()
